        $ranking = $this->createQueryBuilder('u')
            ->select('uu.id as uid, count(uu.id) as count')
            ->join('u.referrer', 'uu')
            ->groupby('uu.id')
            ->orderBy('count', 'DESC')
            ->getQuery()
            ->getResult()
        ;

        foreach ($ranking as $k => $r) {
            $ranking[$k]['user'] = $this->find($r['uid']);
        }
        return $ranking;
    }

